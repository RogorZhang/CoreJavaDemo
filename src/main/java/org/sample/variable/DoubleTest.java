package org.sample.variable;

public class DoubleTest {
	
	public static void main(String[] args) {

		float f1 = 3.1415926f;
		System.out.println("f1 = " + f1); // f1 = 3.1415925
		
		System.out.println("---------------------------------------------------------");
		double d1 = 3.1415926;
		System.out.println("d1 = " + d1); // d1 = 3.1415926
	}
}